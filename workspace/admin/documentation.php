                <p>The adsense shortcode displays your specified adsense ad anywhere in the post. Click on the adsense shortcode button<span class="znadsns ctrl_shrtcd"></span>. It will add this code in your editor:</p>
                <pre style="white-space:pre-line;">[adsense id="3637220125174754" slot="4668915978" width="480" height="60"]</pre>
                <p><strong>There are 4 variables to set:</strong><br /><br />
                    <strong>id</strong>= your adsense id<br /><br />
                    <strong>slot</strong>= your ad slot<br /><br />
                    <strong>width</strong>= width of the ad<br /><br />
                    <strong>height</strong>= height of the ad.</p>
                    
                    
