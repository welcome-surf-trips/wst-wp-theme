                   <h4>The Post Thumbnails</h4>
                   <p>The post thumbnails of Theron works this way: If a post doesn't have a "Featured Image" set, it will search for attached Image of the post and  display it, if the post doesn't have an attached image too the default blank image will be displayed.<br /><br />
                   
                   To set the Featured Image of a post go to Post edtior page and at the right side of the post editor, you will find the Featured Image box. Upload the featured Image and click "use as featured image" link after uploading.
                   </p>
                        
                   <h4>Enabling Video Thumbnails</h4>  
                   
                   <p>With Theron you can easily show your youtube videos as thumbnails on the front page posts instead of images. To enable video thumbnail, while editing the post in the post editor page notice there is a box called "Custom Fields" under the post editor. From the "Name" drop-down list select "videolink" and in the post value put your youtube video id and click "Add Custom Field" button and then click the "Update" button.<br /><br />

<b>e.g:</b> if this is your youtube video link: <b><i>http://www.youtube.com/watch?v=ZMZ323LA3Ko</i></b> &nbsp;&nbsp; your video id is <b>ZMZ323LA3Ko</b></p>                  
                            
