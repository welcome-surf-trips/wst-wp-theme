                   <h4>Setting Up The Page Templates</h4>
                   <p>Theron has 3 Page Templates:</p>
                   <ol>
                    <li>Contact Page</li>
                    <li>Page with Left Sidebar</li>
                    <li>Page with No Sidebar</li>
                    <li>Archive Template</li>
                    <li>Sitemap Template</li>
                    <li>Blog Page Templates</li>
                    </ol>
                   <p>You can select page templates only for Pages. To set up page template for a page in the page editor on the right side you will find a box called "Page Attributes". Select any one from the 3 templates from the <b>Template</b> drop-down menu.</p> 
                   <p><b>To setup the blog page template</b>, you will have to create a new page called "Blog" or anything you like and then assign the "Blog Page" template and then Go to Theron Options> Misc and from the very bottom select the category from which you want to show your blog posts.</p>
                   <br /><br /><br /><br />
                  <small><b>Note:</b>The Contact page does not require any 3rd party plugin or anything. Just select the page template and when people sends messgae through the contact form it will be sent directly to your email(the one you used to setup wordpress) within seconds.</small>
                   
                   
                   
                   
                   
                   
                
