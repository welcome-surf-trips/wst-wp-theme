                	<h4>Setting Up The Slider</h4>
                    <p>Theron comes with unique and easy to use slider. Firrst Go to Settings>Reading and make sure you have set your front page to "Your Latest posts". Then to set up slider go to Slider> Add New</p>
                    <div class="docum_slidermenu"></div>
                	<p><strong>In the "Add New Slide" page there are 4 Boxes:</strong></p>
                    <div class="docum_sliderinter"></div>
                    <div style=""></div>
                    <p><strong>Title:</strong> Enter the title of the slide in the first post.</p>

<p><strong>Slide Image:</strong> Upload the image of the slide in this box. Click on "Set featured image". A pop-up window will appear. Click on Select files button and select your slide image. After uploading the file click on the "Use as featured image" button.<br /><br />
For News Slider and Nivo Slider - The dimension of your slider images should be 1000px by 400px.<br /><br />
For Accordion -  the Images height should be of minimum 480px and the width can be of any size.<br /><br />
</p>

<p><strong>Excerpt:</strong> Write the slide description here. Keep it within 2 lines.</p>

<p><strong>Link:</strong> Set the link of the slide here. This link will be integrated with slide's image and content. The link can be internal or external. If you want to link a slide to one of your posts, put the link of the post here.</p>
                
